<h1>About us</h1>
<p>We are a group of privacy campaigners who believe the internet should be free from state surveillance.  We developed haystak to be a fast and comprehensive search engine because we were not satisfied with the ones already on offer - they frequently indexed too few pages to be particularly useful.   Haystak has a custom crawler developed specifically for the darknet, and hence it is able to find all reachable pages and index them.</p>

<p>Whilst we offer some premium services this is simply to cover our costs - as you know, servers are not free sadly.  They would be if we ran the world but we don't.</p>

<h1>Censorship</h1>
<p>Generally we don't believe in censorship, but we make an exception for any content which is immoral or illegal for us to link to.  Examples would be child abuse and human trafficking.  If you find any of this indexed in our engine click the report link and we'll remove it swiftly.</p>

